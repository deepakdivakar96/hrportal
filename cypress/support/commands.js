Cypress.Commands.add('isElementVisible', (xpath) => {
    return cy.xpath(xpath).then(($el) => {
      // Check if the element exists and is visible
      if ($el.length > 0 && $el.is(':visible')) {
        return true;
      } else {
        return false;
      }
    });
  });
  